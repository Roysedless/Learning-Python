# Print Learning (1)
# ^^ print is a build function to show on screen different word (character) on a string.
print('hello world')  # str (we can use a single quotes)
print("it's good")  # str (to use a single quotes in between we mast use a double quotes)
print('*' * 10)  # str * int [string concatenation]
print("good job " + str(10))  # str + int(str) [string concatenation]
print(f"good job {10}")  # str(int) + int [string formatted]
print("good job " + str(10.1))  # str + float(str) [string concatenation]
print(f"good job {10.1}")  # str(float) + float [string formatted]
