# Print Lesson (1)
# ^^ Print is a build function to show on screen different word (character) that make a string.
print('hello world')  # str (we can use a single quotes)
print("it's good")  # str (to use a single quotes in between we mast use a double quotes)
print('*' * 10)  # str * int [string concatenation just Multiplication a str]
print("good job " + str(10))  # str + str(int) [string concatenation]
print(f"good job {10}")  # "str", {int,float,variables} > str [string formatted]
print("good job " + str(10.1))  # str + str(float) [string concatenation]
print(f"good job {10.1}")  # "str", {int,float,variables} > str [string formatted]
