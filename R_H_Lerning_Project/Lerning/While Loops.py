# While Loop (Learning)
# ^^ While Loop repeatedly executed a condition.
# ^ basic example (print 1-5 with using a while loop)>>
i = 1  # defining a variables
while i <= 5:  # set a while loop with his condition
    print(i)  # print the variables
    i = i + 1  # change the variables value to the next printed value
print('Done')  # set an end message when the while loop done
# ^ make it more interesting (with repeated a string)
string_i = 1
while string_i <= 5:
    print('*' * string_i)
    string_i = 1 + string_i
print('Done')
