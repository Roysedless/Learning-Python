# For Loop (Learning)
# ^^ we use that to iterate over items of a collection.
#    such as a string (sequence of characters)
for item in 'Python':  # using in for a string character
    print(item)
for item in ['roy', 'sad', 'good']:  # using in for a list
    print(item)
# ^^ And: [1, 2, 3, 4, 5]
for item in range(5):  # using in for a range
    # ^^ range is an object that changed every iteration.
    print(item)
# ^^ And: range (5, 10)
#      range (5, 10, 2) the third number is defined,
#      the amount of steps in all characters.
